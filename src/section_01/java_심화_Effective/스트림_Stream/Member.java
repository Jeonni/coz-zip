package section_01.java_심화_Effective.스트림_Stream;

// 아래 코드는 변경하지 마세요.
public class Member {
    String name;
    String gender;
    int age;

    public Member(String name, String gender, int age) {
        this.name = name;
        this.gender = gender;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public String getGender() {
        return gender;
    }

    public int getAge() {
        return age;
    }
}